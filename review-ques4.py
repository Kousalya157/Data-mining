import itertools

# Define the list
elements = ['A', 'B', 'C', 'D', 'E']

# I. Compute all combinations
print("Combinations:")

# Generate combinations for length 2 to 5 
for r in range(2, len(elements) + 1):
    combinations = itertools.combinations(elements, r)
    for combination in combinations:
        print(combination)
print("\n")

# II. Compute all permutations
print("Permutations:")

# Generate permutations for length 2 to 5 
for r in range(2, len(elements) + 1):
    permutations = itertools.permutations(elements, r)
    for permutation in permutations:
        print(permutation)

